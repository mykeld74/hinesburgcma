<section class="pageSection">
	<p class="pageSectionText">
		Attending church on a Sunday morning to worship as a full congregation is a wonderful and vital
		part of the Christian faith. However, getting to know others on a personal level and developing
		real friendships is far more fruitful when done through involvement in smaller groups. At CAC,
		there are myriad opportunities to join with other adults who share your interests and faith
		goals. We have groups for men, groups for women, and co-ed groups. Some groups meet in someone's
		home to share a meal and then do a Bible study, while some groups meet out at a restaurant for a
		quick devotional during the workday. Some groups meet for years on a regular basis, while others
		gather for certain events or when a particular need arises. Whatever the situation, adult
		ministries at CAC exist to provide each person with opportunities to <em>live life</em> with
		others in our church family. By <em>living life together</em>, we mean that we have developed
		relationships through which we know about and participate in what's happening in one another's
		lives, homes and hearts. Please read on for information on some of the adult ministries at CAC!
	</p>
</section>

<section class="pageSection">
	<h2 class="pageSectionTitle">Men's Ministry</h2>
	<p class="pageSectionText">
		Men's Ministry at CAC exists to encourage and support our fellow man in his pursuit of Christ.
		We meet twice a month for our Men's Group, during which we eat dinner together, discuss what's
		going on in our lives, study the Bible and pray. Outside of this bi-monthly meeting time, we
		also do service projects and other activities to allow us to live out our faith. Ultimately, we
		seek to live the life God has intended for each of us.
	</p>
	<p class="pageSectionText">
		If you would like to know more about Men's Ministry at CAC, please email us, or visit our Small
		Groups page to learn more about all of our active Small Groups.
	</p>
