<section class="pageSection">
	<h2 class="pageSectionTitle">Women's Ministry</h2>
	<p class="pageSectionText">
		Women's Ministry at CAC exists to provide opportunites for women to find a place where they can
		connect with other women, to find hope and help in all of life. Whether it's through different
		women's groups or Bible studies, intentional small group gatherings, special events and service
		opportunities, our goal is to grow together. Our hope is that women will develop deep
		relationships with each other, grow in their faith in Jesus and find a place of true acceptance
		and love that will help them go out and offer the same to those around them.
	</p>
