<section class="pageSection">
	<h2 class="pageSectionTitle">Small Groups</h2>
	<p class="pageSectionText">
		CAC Small Groups (SGs) are an opportunity to practice following Jesus in smaller groups that
		meet regularly to study, share, and serve with each other. The schedule, venue and focus vary by
		group. The intention of SGs is to build each other up in ways that are best done in a setting of
		several people, as opposed to a full congregation. The goal of SGs is to see each other grow in
		faith, while building solid, authentic relationships that last a lifetime.
	</p>
	<p class="pageSectionText">
		To learn more about SGs, contact Ray Bulaga, co-coordinator of our Small Group team, at
