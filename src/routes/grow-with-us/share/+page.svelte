<section class="pageSection">
	<h2 class="pageSectionTitle">Five Steps to Share My Faith</h2>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>1. Stay as close to Jesus as you possibly can</strong> and pray that you will experience
			his love in a way that will spill over naturally into the lives of those he brings into your life.
			You can do this by staying in his word daily and asking other believers to pray with you and help
			you share your faith. (See John 15)
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>2. Go make some friends!</strong> Seriously! People want to know about Jesus way more than
			you could ever imagine. The world is full of these people, so go find them. Ask the Holy Spirit
			to guide you to "people of peace" (Mt 10 & Lk 10) and let God's love flow through your life and
			watch those "good deeds" take root. (Mt. 5:14-16)
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>3. Open your mouth like Jesus did!</strong> Jesus kept it both simple and profound. He
			didn't convince people through arguments of reason or philosophy or even science. He could
			have, but instead he surprisingly brought it <strong>back to morality.</strong> And humanity's
			answer to the morality problem is to either: a) Be "good enough"; or b) if you can't do that,
			just redefine morality and get others to agree with you. The result is that most of us either
			way, end up thinking we're <strong>pretty</strong> <em>GOOD people</em> which prevents us from
			becoming <strong>truly</strong> <em>SAVED people</em>. (Mk 10:17-27 & Jn. 3:1-21)
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>4. Don't go it alone!</strong> We are a church for <strong>REAL Life</strong>! Jesus
			was all about REAL Life! He <em>could</em> have shared the Gospel alone, but he chose to have
			a
			<strong>real</strong> team serving together as a <strong>real</strong> "three-dimensional"
			(3-D) family. Jesus said <em>"I came to give them life, a</em> <strong><em>real</em></strong>
			<em>and eternal life."</em> Jesus lived a <strong>real life</strong> in three dimensions:
			<strong>UP, IN, and OUT.</strong> So ask the Pastor or the other elders to visit and pray with
			you about God's Vision for CAC,
			<a href="https://www.icloud.com/iclouddrive/098htgCbPaB_Yo-kH4o8a7bhg#CAC_Vision_copy"
				>3-D Huddles & 3-D Families</a
			>. This is the <strong>future of CAC</strong>;
			<em>making disciples who make disciples in every day, real life.</em>
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>5. Practice, practice, practice!</strong> You can never get enough practice. Here's some
			important tips on HOW to practice:
		</p>
		<ol class="pageSectionList">
			<li class="pageSectionListItem">
				<strong>Go to this</strong>
				<a href="/grow-with-us/follow/">LINK</a>
				<strong>and start sharing the</strong> <strong>4 steps to follow Jesus</strong> in your own words
				along with the Bible verses. This is much easier than you think. Practice with anyone who will
				listen, including other believers who can take turns with you.
			</li>
			<li class="pageSectionListItem">
				<strong>Watch as many</strong>
				<a href="https://www.youtube.com/watch?v=ThjMa7CK0sA">Ray Comfort interviews</a>
				<strong>on YouTube</strong> as you can get your hands on. He has hundreds of short videos that
				are so much fun and you will be refreshed and inspired to share your faith like Jesus did. It's
				counter-intuitive, radically inclusive, and politically incorrect. And that's exactly how Jesus
				chose to love people.
			</li>
			<li class="pageSectionListItem">
				<strong>Study up on the hot topics</strong> that we humans use to push God away. i.e.
				<em
					>"sexual orientation, abortion, gender, inclusivity, credibility of the Bible, science vs.
					faith, etc."</em
				> <strong>Here's a</strong>
				<a href="https://www.youtube.com/watch?v=_kWPA8dLR7U">good place</a>
				<strong>to start for teens... OK, actually for ALL of us.</strong> But
				<strong>here's a warning</strong>: Being an <em>expert</em> WILL do more harm than good!
				Please don't attempt to <em>convince</em> people. Only the Holy Spirit can open a human
				heart to Jesus. Just be prepared to give one or two thoughtful, intelligent, and informative
				answers on these matters in order to help move the conversation
				<strong>back to our morality</strong>
				as the basis for discovering <strong>REAL Life</strong> in Jesus.
			</li>
		</ol>
	</div>
</section>
