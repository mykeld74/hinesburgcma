<section class="pageSection">
	<h2 class="pageSectionTitle">HOW TO FOLLOW JESUS</h2>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>1. God's plan — A Real Life.</strong> God loves you and wants you to experience the
			peace and life He offers. He has a plan for you. The Bible says, "<em
				>For God so loved the world that He gave His only begotten Son, that whoever believes in Him
				should not perish but have everlasting life</em
			>" (John 3:16).
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>2. Our problem — Separation From God.</strong> By nature, we are all separated from
			God. The Bible says, "<em>For all have sinned and fall short of the glory of God</em>" (Romans
			3:23, NKJV). He is holy, but we are human and don't measure up to His perfect standard. We are
			sinful, and "<em>the wages of sin is death</em>" (Romans 6:23).
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>3. God's remedy — The Cross.</strong> God's love bridges the separation between you
			and Him. When Jesus Christ died on the cross and rose from the grave, He paid the penalty for
			your sins. The Bible says, "'<em
				>He himself bore our sins' in his body on the cross, so that we might die to sins and live
				for righteousness; 'by his wounds you have been healed</em
			>'" (1 Peter 2:24).
		</p>
	</div>

	<div class="stepSection">
		<p class="pageSectionText">
			<strong>4. Our response — receive Christ.</strong> You cross the bridge into God's family when
			you accept Christ's free gift of salvation. The Bible says, "<em
				>But to all who did receive him, who believed in his name, he gave the right to become
				children of God</em
			>" (John 1:12).
		</p>
	</div>
</section>

<section class="pageSection">
	<h2 class="pageSectionTitle">To receive Christ, a person needs to do four things:</h2>
	<ol class="pageSectionList">
		<li class="pageSectionListItem">Admit that you are a sinner and you need the Savior.</li>
		<li class="pageSectionListItem">
			Be willing to turn from your sins, and ask God to forgive you.
		</li>
		<li class="pageSectionListItem">
			Believe that Jesus Christ died for you on the cross and rose from the grave.
		</li>
		<li class="pageSectionListItem">
			Invite Jesus Christ to come in and control your life through the Holy Spirit.
		</li>
	</ol>
</section>

<section class="pageSection">
	<blockquote class="pageSectionQuote">
		<p>
			Dear God, I know that I am a sinner. I want to turn from my sins, and I ask for Your
			forgiveness. I believe that Jesus Christ is Your Son. I believe He died for my sins and that
			You raised Him to life. I want Him to come into my heart and to take control of my life. I
			want to trust Jesus as my Savior and follow Him as my Lord from this day forward. In Jesus'
			Name, amen.
		</p>
	</blockquote>

	<div class="warningBox">
